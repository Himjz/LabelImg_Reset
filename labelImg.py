    def scroll_request(self, value, orientation):
        # 确保使用正确的Qt.Orientation枚举值
        if orientation == 0:  # 处理可能传入的整数0（对应Horizontal）
            orientation = Qt.Horizontal
        elif orientation == 1:  # 处理可能传入的整数1（对应Vertical）
            orientation = Qt.Vertical

        # 检查scroll_bars是否已正确初始化
        if not hasattr(self, 'scroll_bars'):
            self.scroll_bars = {
                Qt.Horizontal: self.scrollArea.horizontalScrollBar(),
                Qt.Vertical: self.scrollArea.verticalScrollBar()
            }

        # 确保方向键存在于字典中
        if orientation not in self.scroll_bars:
            return

        bar = self.scroll_bars[orientation]
        # 调整滚动值（根据需要调整比例因子）
        bar.setValue(bar.value() - value // 10)

